typedef word_t Elf32_Half;
typedef dword_t Elf32_Word;
typedef int Elf32_Sword;
typedef dword_t Elf32_Addr;
typedef dword_t Elf32_Off;

#define EI_NIDENT 16
typedef struct {
    byte_t e_ident[EI_NIDENT];
    Elf32_Half e_type;
    Elf32_Half e_machine;
    Elf32_Word e_version;
    Elf32_Addr e_entry;
    Elf32_Off  e_phoff;
    Elf32_Off  e_shoff;
    Elf32_Word e_flags;
    Elf32_Half e_ehsize;
    Elf32_Half e_phentsize;
    Elf32_Half e_phnum;
    Elf32_Half e_shentsize;
    Elf32_Half e_shnum;
    Elf32_Half e_shstrndx;
}__attribute__((packed)) Elf32_Ehdr_t;

typedef struct {
    Elf32_Word p_type;
    Elf32_Off  p_offset;
    Elf32_Addr p_vaddr;
    Elf32_Addr p_paddr;
    Elf32_Word p_filesz;
    Elf32_Word p_memsz;
    Elf32_Word p_flags;
    Elf32_Word p_align;
}__attribute__((packed)) Elf32_Phdr_t;

#define ELF_PT_LOAD 1

#define ELF_ET_EXEC 2
#define ELF_ET_DYN 3

#define ELF_PF_X 0x1
#define ELF_PF_W 0x2
#define ELF_PF_R 0x4