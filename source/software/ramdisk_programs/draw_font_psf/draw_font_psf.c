// TODO: read .psf format from ramdisk, parse it and draw it
#include <hardware/controllers/graphic/default_graphic_card/default_graphic_card.h>

void draw_char_psf(dword_t monitor_id, dword_t ch, dword_t x, dword_t y, dword_t color) {
    if(ch >= 128) {
        ch = 30;
    }

    dword_t mode = go_get_actual_mode(monitor_id);
    dword_t width = go_get_width_of_mode(monitor_id, mode);
    dword_t height = go_get_height_of_mode(monitor_id, mode);
    dword_t *buff = (go_get_double_buffer(monitor_id) + y*width*4 + x*4);

    if(x >= width || y >= height) {
        return;
    }

    dword_t draw_lines = ((y + 8) < height) ? 8 : (height - y);
    dword_t draw_columns = ((x + 8) < width) ? 8 : (width - x);
    const byte_t *character = &bleskos_binary_font[ch * 8];
    for (int i = 0; i < draw_lines; i++) {
        byte_t line = *character;
        for (int j = 0; j < draw_columns; j++) {
            dword_t mask = 1u << (7 - j);
            if(line & mask) {
                *buff = color;
            }
            buff++;
        }
        character++;
        buff += (width - draw_columns);
    }
}

void print_psf(dword_t monitor_id, char *string, dword_t x, dword_t y, dword_t color) {
    dword_t x_base = x;
    while(*string != '\0') {
        if(*string == '\n') {
            x = x_base;
            y += 10;
        }
        else {
            draw_char_psf(monitor_id, *string, x, y, color);
            x += 8;
        }
        string++;
    }
}